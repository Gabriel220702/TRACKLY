        <h4>¿Qué es TRACKLY?</h4>
        <p>TRACKLY es un avanzado dispositivo de monitoreo y geolocalización, diseñado para el seguimiento controlado y seguro de personas menores de edad. Integrado con herramientas de alta precisión, permiten un monitoreo continuo y detallado de la ubicación del usuario. Su operatividad se fundamenta en la implementación de tecnología de vanguardia, incluyendo sensores y algoritmos de procesamiento de datos, que permiten observar el lugar donde se encuetra el menor en tiempo real. </p>
      </div>
      <div class="col-md-4">
        <h4>¿Qué ofrecemos?</h4>
        <p>TRACKLY ofrece un dispositivo versatil, avanzado y configurable a una pulsera, llavero o dispositivo de bolsillo, para la geolocalización y alerta de peligro. Cuenta con tres diseños únicos y ergonómicos para ayudar y proporcionar información de los usuarios a la persona a cargo, así mismo el optimizar la capacidad de respuesta ante eventos críticos, minimizando el riesgo de peligros o extravíos.</p>
      </div>
      <div class="col-md-4">
        <h4>¿Porqué escojernos?</h4>
        <p>TRACKLY se presenta como una solución innovadora y proactiva ante la problematica mundial relacionada a extravíos. Diseñado con un enfoque en inclusión y equidad, el dispositivo garantiza que las personas menores reciban una asistencia de manera cómoda y segura a través de sus tres opciones, tal como la pulsera, el llavero o el dispositivo de bolsillo, todos con implementación de una detección temprana y una respuesta efectiva por medio de un sensor avanzado.</p>
